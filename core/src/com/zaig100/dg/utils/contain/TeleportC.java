package com.zaig100.dg.utils.contain;

public class TeleportC {
    int x, y, tx, ty;

    public TeleportC(int x, int y, int tx, int ty) {
        this.x = x;
        this.y = y;
        this.tx = tx;
        this.ty = ty;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getTx() {
        return tx;
    }

    public int getTy() {
        return ty;
    }
}
