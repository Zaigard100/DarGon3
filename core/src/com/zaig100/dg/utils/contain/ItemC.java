package com.zaig100.dg.utils.contain;

public class ItemC {
    int x, y, id;

    public ItemC(int x, int y, int id) {
        this.x = x;
        this.y = y;
        this.id = id;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getId() {
        return id;
    }
}
