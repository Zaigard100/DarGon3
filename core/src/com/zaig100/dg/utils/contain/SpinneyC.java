package com.zaig100.dg.utils.contain;

public class SpinneyC {
    int x, y, wight, height;

    public SpinneyC(int x, int y, int wight, int height) {
        this.x = x;
        this.y = y;
        this.wight = wight;
        this.height = height;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWight() {
        return wight;
    }

    public int getHeight() {
        return height;
    }
}
