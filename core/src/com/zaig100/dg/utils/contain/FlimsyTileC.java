package com.zaig100.dg.utils.contain;

public class FlimsyTileC {
    int x, y, stage;

    public FlimsyTileC(int x, int y, int stage) {
        this.x = x;
        this.y = y;
        this.stage = stage;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getStage() {
        return stage;
    }
}
